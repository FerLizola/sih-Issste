    private void txtEdadKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtEdadKeyReleased
        try{    
        int edad=Integer.parseInt(txtEdad.getText());
           if(edad > 0 && edad < 4){
          txtPerTorax.setEnabled(true);
          txtPerCef.setEnabled(true);
          txtPresion.setEnabled(false);
        }else {
            txtPerTorax.setEnabled(false);
            txtPerCef.setEnabled(false);
            txtPresion.setEnabled(true);
           }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Ingrese la edad");
        }
    }//GEN-LAST:event_txtEdadKeyReleased

