    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         if(txtRFCD.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "El campo RFC se encuentra vacío, por favor llenelo");
        }else{
        TDA_Expedientes te=new TDA_Expedientes(txtRFCD.getText());
        if(te.numExpediente()){
            txtNumExp.setText(te.getNumExp());
        }else{
                showMessageDialog(null,"No encontrado");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void txtFechaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtFechaMouseClicked
        Calendar c1 = Calendar.getInstance();
        int an=(c1.get(Calendar.YEAR));
        int mes=c1.get(Calendar.MONTH)+1;
        int dia= c1.get(Calendar.DAY_OF_MONTH);
        txtFecha.setText(an +"-"+ mes +"-" +dia);
    }//GEN-LAST:event_txtFechaMouseClicked

    private void txtHoraMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtHoraMouseClicked
       Calendar c1 = Calendar.getInstance();
        int hora=(c1.get(Calendar.HOUR));
        int minutos=c1.get(Calendar.MINUTE);
        txtHora.setText(hora +":"+ minutos);
    }//GEN-LAST:event_txtHoraMouseClicked
                                
