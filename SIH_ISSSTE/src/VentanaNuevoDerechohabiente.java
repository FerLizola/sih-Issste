                    txtEstado.setText(r.getString("EstadoRep_Derechoh"));
                    txtMunicipio.setText(r.getString("MunicipioRep_Derechoh"));
                    txtLocal.setText(r.getString("LocalidadRep_Derechoh"));
                    txtTel.setText(r.getString("TelefonoCasa_Derechoh"));
