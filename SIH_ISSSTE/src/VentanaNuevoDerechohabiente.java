    private void jRadioButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButton5ActionPerformed
        String contenido = ""; 
        String[] letras = {"D","E","F","G","H","I","J","K","L","M"};
        int contador = -1;
        if(txtRfc.getText().length() == 13){
            
            Conexion_BD cn = new Conexion_BD();
            rs = cn.obtenerRFCDerechohabienteRegistroHijos(txtRfc.getText());
            
            try {
                if(rs.next()== false){
                    javax.swing.JOptionPane.showMessageDialog(this, "No se encontro coincidencias");
                }else{
                    rs.beforeFirst();
                    while(rs.next()){
                        contenido = contenido + rs.getString("RFC_Derechoh") + ",";
                    }
                    contenido = contenido.substring(0, contenido.length()-1);
                    
                    String[] vectorContenido = contenido.split(",");
                    
                    for(int i = 0; i<vectorContenido.length;i++){
                        if(vectorContenido[i].matches(".*[D-M]")){
                            contador++;
                        }
                    }
                    
                    if(contador == -1){
                        txtRfc.setText(txtRfc.getText() + "D");
                    }else{
                        txtRfc.setText(txtRfc.getText() + letras[contador + 1]);
                    }
                    
                    
                }
            } catch (SQLException ex) {
                Logger.getLogger(VentanaNuevoDerechohabiente.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else if(txtRfc.getText().length() == 14){
            txtRfc.setText(txtRfc.getText().substring(0, txtRfc.getText().length()-1));
            Conexion_BD cn = new Conexion_BD();
            rs = cn.obtenerRFCDerechohabienteRegistroHijos(txtRfc.getText());
            
            try {
                if(rs.next()== false){
                    javax.swing.JOptionPane.showMessageDialog(this, "No se encontro coincidencias");
                }else{
                    rs.beforeFirst();
                    while(rs.next()){
                        contenido = contenido + rs.getString("RFC_Derechoh") + ",";
                    }
                    contenido = contenido.substring(0, contenido.length()-1);
                    
                    String[] vectorContenido = contenido.split(",");
                    
                    for(int i = 0; i<vectorContenido.length;i++){
                        if(vectorContenido[i].matches(".*[D-M]")){
                            contador++;
                        }
                    }
                    
                    if(contador == -1){
                        txtRfc.setText(txtRfc.getText() + "D");
                    }else{
                        txtRfc.setText(txtRfc.getText() + letras[contador + 1]);
                    }
                    
                    
                }
            } catch (SQLException ex) {
                Logger.getLogger(VentanaNuevoDerechohabiente.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else{
            javax.swing.JOptionPane.showMessageDialog(this, "Ingrese un RFC valido por favor");
            txtRfc.setText("");
            txtRfc.requestFocus();
            buttonGroup1.clearSelection();
        }
        
    }//GEN-LAST:event_jRadioButton5ActionPerformed

